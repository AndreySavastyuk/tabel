# -*- coding: utf-8 -*-
"""Производственный календарь РФ: федеральные праздники (ст. 112 ТК) +
официальные переносы выходных по годам.

Праздники несдвигаемы; переносы (перенесённые выходные и рабочие субботы)
утверждаются правительством ежегодно. Для годов, которых нет в TRANSFERS,
переносы кадровик заносит вручную в экране календаря.
"""
from datetime import date

# (месяц, день): новогодние каникулы 1–8 янв, Рождество 7 янв включительно;
# 23.02, 08.03, 01.05, 09.05, 12.06, 04.11.
_FIXED = [(1, d) for d in range(1, 9)] + [(2, 23), (3, 8), (5, 1), (5, 9), (6, 12), (11, 4)]

# Официальные переносы по годам: dayoff — перенесённые выходные (нерабочие),
# workday — рабочие субботы (перенос на рабочий).
# 2026: Постановление Правительства РФ № 1466 от 24.09.2025 + ст.112 (праздник
# на сб/вс → следующий рабочий день): 3 янв(сб)→9 янв, 4 янв(вс)→31 дек,
# 8 мар(вс)→9 мар, 9 мая(сб)→11 мая. Рабочих суббот в 2026 нет.
TRANSFERS = {
    2026: {
        "dayoff": [date(2026, 1, 9), date(2026, 3, 9), date(2026, 5, 11), date(2026, 12, 31)],
        "workday": [],
    },
}


def federal_holidays(year: int) -> list[date]:
    return [date(year, m, d) for m, d in _FIXED]


def transfers(year: int) -> tuple[list[date], list[date]]:
    """(перенесённые выходные, рабочие субботы) для года; ([], []) если нет."""
    t = TRANSFERS.get(year, {})
    return list(t.get("dayoff", [])), list(t.get("workday", []))
